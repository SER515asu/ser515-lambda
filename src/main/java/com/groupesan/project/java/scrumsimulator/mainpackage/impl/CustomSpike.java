package com.groupesan.project.java.scrumsimulator.mainpackage.impl;

public class CustomSpike {
    private String spikeTitle;
    private String spikeDescription;

    public CustomSpike(String spikeTitle, String spikeDescription) {
        this.spikeTitle = spikeTitle;
        this.spikeDescription = spikeDescription;
    }

    public String getSpikeTitle() {
        return spikeTitle;
    }

    public String getSpikeDescription() {
        return spikeDescription;
    }

    
}
